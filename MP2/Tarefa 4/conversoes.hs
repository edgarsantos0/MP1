farheitTocelsius :: Double -> Double
farheitTocelsius x = (x - 32) * 5/9

celsiusTofarenheit :: Double -> Double
celsiusTofarenheit x = (x * 1.8) + 32

feetTometers :: Double -> Double
feetTometers x = x * 0.3048

metersTofeet :: Double -> Double
metersTofeet x = x * 3.28084

milesTokilometer :: Double -> Double
milesTokilometer x = x * 1.609344

kilometerTomile :: Double -> Double
kilometerTomile x = x * 0.6214

